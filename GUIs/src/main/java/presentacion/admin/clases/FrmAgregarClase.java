    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        try {
            // Crear el objeto ClaseDTO
            ClaseDTO clase = new ClaseDTO();
            clase.setNombre(txtNombre.getText());
            clase.setSemestre(Integer.parseInt(txtSemestre.getText()));

            // Confirmaci√≥n en consola (opcional)
            System.out.println("Clase creada: " + clase);

            subsClase.agregarClase(clase);

            JOptionPane.showMessageDialog(this, "La clase se ha agregado correctamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            FrmGestionarClase gestionarClase = new FrmGestionarClase();
            gestionarClase.setVisible(true);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error inesperado: " + e, "Ha ocurrido un Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

