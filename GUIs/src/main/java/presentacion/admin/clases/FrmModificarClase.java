    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        try {
            // Crear el objeto ClaseDTO
            ClaseDTO claseModificada = new ClaseDTO();
            claseModificada.setNombre(txtNombre.getText());
            claseModificada.setSemestre(Integer.parseInt(txtSemestre.getText()));

            // Confirmaci√≥n en consola (opcional)
            System.out.println("Clase creada para modificar: " + claseModificada);

            subsClase.modificarClase(claseRecibida, claseModificada);

            JOptionPane.showMessageDialog(this, "La clase se ha modificado correctamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            FrmGestionarClase frmGestionarClase = new FrmGestionarClase();
            frmGestionarClase.setVisible(true);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error inesperado: " + e, "Ha ocurrido un Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAceptarActionPerformed


